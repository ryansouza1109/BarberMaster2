    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
      
       try { 
        
      String nome = txtNome.getText();
      String CPF = txtCPF.getText();
      String telefone = txtTelefone.getText();
      String cargo = (String) CBCargo.getSelectedItem();
      String senha = txtSenha.getText();
      String confirmarSenha = txtConfirmarSenha.getText();
      
       boolean cpfValido = CPF.matches("\\d{11}|\\d{3}\\.\\d{3}\\.\\d{3}-\\d{2}");
       boolean telefoneValido = telefone.matches("\\(\\d{2}\\) \\d{5}-\\d{4}|\\d{11}");
       boolean senhaValida = senha.equals(confirmarSenha);
       
        if (!cpfValido) {
        JOptionPane.showMessageDialog(null, "CPF inválido! Use 000.000.000-00 ou 00000000000.");
        return;
    }
    
    if (!telefoneValido) {
        JOptionPane.showMessageDialog(null, "Telefone inválido! Use (XX) XXXXX-XXXX ou XXXXXXXXXXX.");
        return;
    }
    
    if (!senhaValida) {
        JOptionPane.showMessageDialog(null, "As senhas não coincidem!");
        return;
    }
    
       Funcionario funcionario;
      
      if (cargo.equals("Gerente")) {
        funcionario = new Gerente(nome, CPF, senha, telefone);
      
    } else if (cargo.equals("Barbeiro")) {
        String especialidade = JOptionPane.showInputDialog(null, "Digite a sua especialidade:");
        funcionario = new Barbeiro(nome, CPF, senha, especialidade, telefone, cargo);
    } else {
            JOptionPane.showMessageDialog(null, "Cargo inválido!");
            return;
        }
      
      
         FuncionarioDAO dao = new FuncionarioDAO();
        boolean sucesso = dao.cadastrar(funcionario); 

            if (sucesso) {
            JOptionPane.showMessageDialog(null, "Funcionário cadastrado com sucesso!");

            if (cargo.equals("Gerente")) {
                TelaEscolhaGerente t = new TelaEscolhaGerente();
                t.setVisible(true);
                 }
              } else {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar o funcionário.");
                  }
              } catch (Exception e) {
              JOptionPane.showMessageDialog(null, "Erro ao cadastrar: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                  }
      
    }//GEN-LAST:event_btnSalvarActionPerformed

